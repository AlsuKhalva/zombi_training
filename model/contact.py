class Contact:

    def __init__(self, first_name=None, last_name=None, mobil=None, email=None, day=None,
                     month=None, year=None, notes=None, id=None):
        self.first_name = first_name
        self.last_name = last_name
        self.mobil = mobil
        self.email = email
        self.day = day
        self.month = month
        self.year = year
        self.notes = notes
        self.id = id